void HttpRequest::feed(char *pBuffer, size_t pSize)
{
	char	ch;

	for (size_t i = 0; i < pSize; i++)
	{
		ch = pBuffer[i];
		switch (CURRENT_STATE()) {
			case REQ_START:
			{
				UPDATE_STATE(REQ_METHOD);
				break;
			}
			case REQ_METHOD:
			{
				std::cout << "method GET" << std::endl;
				UPDATE_STATE(REQ_SPACE_BEFORE_URI);
				break;
			}
			default:
				break;
		}
	}
}

