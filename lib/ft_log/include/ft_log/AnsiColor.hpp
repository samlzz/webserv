// ANSI color codes
// ============================================================================

# define FT_LOG_COLOR_RESET			"\033[0m"
# define FT_LOG_COLOR_BLACK			"\033[30m"
# define FT_LOG_COLOR_RED			"\033[31m"
# define FT_LOG_COLOR_GREEN			"\033[32m"
# define FT_LOG_COLOR_YELLOW		"\033[33m"
# define FT_LOG_COLOR_BLUE			"\033[34m"
# define FT_LOG_COLOR_MAGENTA		"\033[35m"
# define FT_LOG_COLOR_CYAN			"\033[36m"
# define FT_LOG_COLOR_WHITE			"\033[37m"

# define FT_LOG_COLOR_BOLD_BLACK	"\033[1;30m"
# define FT_LOG_COLOR_BOLD_RED		"\033[1;31m"
# define FT_LOG_COLOR_BOLD_GREEN	"\033[1;32m"
# define FT_LOG_COLOR_BOLD_YELLOW	"\033[1;33m"
# define FT_LOG_COLOR_BOLD_BLUE		"\033[1;34m"
# define FT_LOG_COLOR_BOLD_MAGENTA	"\033[1;35m"
# define FT_LOG_COLOR_BOLD_CYAN		"\033[1;36m"
# define FT_LOG_COLOR_BOLD_WHITE	"\033[1;37m"

#endif // FT_LOG_ANSI_COLOR_HPP

